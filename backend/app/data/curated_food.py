def get_curated_food(city: str) -> List[POIInfo]:
    out: List[POIInfo] = []
    for row in _FOOD.get(city or "", []):
        poi = POIInfo(
            id=row["id"],
            name=row["name"],
            type="餐饮服务",
            address=row["address"],
            location=Location(longitude=row["lng"], latitude=row["lat"]),
            rating=row.get("rating"),
            cost=str(row.get("cost") or ""),
            biz_type="餐饮",
            typecode="050000",
        )
        # 附加轻量元数据,不进入接口 schema,仅后端内部使用。
        object.__setattr__(poi, "_meal_types", row.get("meal_types", []))
        object.__setattr__(poi, "_areas", row.get("areas", []))
        object.__setattr__(poi, "_signature", row.get("signature", "招牌菜"))
        object.__setattr__(poi, "_note", row.get("note", "当地人气餐厅"))
        out.append(poi)
    return out
